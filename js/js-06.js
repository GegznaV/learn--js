/* === Variable creation === */

/*

Variable creation consists of 2 steps:
1) declaration
2) initialization

*/ 

// both steps as a single line
var age = 50;

// both steps as separate lines
var age2;
age2 = 50;

// In case of numbers, a copy is created:
var newAge = age;

// Expression
var message = "newAge = " + newAge; // newAge = 50
